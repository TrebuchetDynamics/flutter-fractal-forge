FractalModule _buildLegalizeFotdModule(SharedLegalizeFotdEntry entry) {
  return buildEscapeTimeModule(
    EscapeTimeConfig(
      id: entry.id,
      name: entry.name,
      shaderAsset:
          'shaders/escape_time_family/experimental_named/polynomial_variants/legalize_fotd_muth_gpu.frag',
      category: 'Escape-Time',
      defaultIterations: entry.iterations,
      defaultBailout: entry.bailout,
      defaultCenterX: entry.centerX,
      defaultCenterY: entry.centerY,
      defaultZoom: entry.zoom,
      maxIterations: 220,
      extraParams: [
        _floatParam(
          'variant',
          'Formula variant',
          0,
          32,
          1,
          entry.variant.toDouble(),
        ),
        _floatParam('coeffA', 'Coefficient a', -180, 180, 0.01, entry.coeffA),
        _floatParam('powerB', 'Power b', -180, 180, 0.01, entry.powerB),
        _floatParam('coeffD', 'Coefficient d', -180, 180, 0.01, entry.coeffD),
        _floatParam('powerF', 'Power f', -180, 180, 0.01, entry.powerF),
        _floatParam('coeffG', 'Coefficient g/k', -16, 16, 0.01, entry.coeffG),
        _floatParam('powerH', 'Power h', -16, 16, 0.01, entry.powerH),
        _floatParam('coeffJ', 'Coefficient j', -16, 16, 0.01, entry.coeffJ),
        _floatParam('powerK', 'Power k', -16, 16, 0.01, entry.powerK),
        _floatParam('seedX', 'Seed/branch real', -16, 16, 0.01, entry.seedX),
        _floatParam('seedY', 'Seed imaginary', -16, 16, 0.01, entry.seedY),
      ],
    ),
  );
}

